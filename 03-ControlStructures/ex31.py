x = 0
y = 0

if x > 0 < y:
    print(f'The point ({x},{y}) is in the first quadrant')
    
if x < 0 and y > 0:
        print(f'The point ({x},{y}) is in the second quadrant')

if x < 0 > y:
        print(f'The point ({x},{y}) is in the third quadrant')
        
if x > 0 and y < 0:
        print(f'The point ({x},{y}) is in the fourth quadrant')
        
if x == 0 or y == 0:
    print('at least one of the points is equal to 0')